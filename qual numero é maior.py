nun1 = float (input ('Digite algum numero '))
nun2 = float (input ('Digite outro numero '))

if nun1 > nun2 :
     print ('o primeiro numero é maior.')
elif nun1 < nun2 :
     print ('O segundo numero é maior')
elif nun1 == nun2 :
     print('Ambos os numeros são iguais.')
else: 
     print('Erro de digitação, digitou algo imcompativel ') 